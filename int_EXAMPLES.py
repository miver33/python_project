# int - целочисленный тип
# 3 вида деления:
# 1 тип - обычное деление (результатом всегда будет float) /
print(15 / 4)
print(15 / 3) # результатом всегда будет float

# следующие 2 типа раотают ТОЛЬКО для интов(int)
# 2 тип - целочисленное деление (получение целой части от деления) обозначается //
print(15 // 4)
# 3 тип - остаток от деления %
print(15 % 4)
